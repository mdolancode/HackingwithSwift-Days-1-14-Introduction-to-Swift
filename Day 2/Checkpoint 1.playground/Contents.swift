import Cocoa

// Create a constant holding any temperature in Celcius.
let temperatureInCelcius = 30.0

// Converts that temperature to Fahrenheit by multipling by 9, dividing by 5 then adding 32

let temperatureInFahrenheit = temperatureInCelcius * 9.0 / 5.0 + 32.0

print("The temperature in Celcius is \(temperatureInCelcius)°C.")
print("The temperature in Fahrenheit is \(temperatureInFahrenheit)°F.")


